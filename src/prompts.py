MODES = {
    "เชิงกลยุทธ์": "เน้นเงื่อนไขการชนะ ภัยคุกคาม และ 2–3 ท่าที่ดีที่สุด",
    "เชิงการสอน": "อธิบายเหตุผลทีละขั้นตอนด้วยภาษาง่ายๆ สำหรับมือใหม่",
    # "เชิงรายละเอียด": "แจกแจงรายละเอียดที่พบบนกระดาน แล้วเสนอท่าพร้อมข้อดี-ข้อเสีย",
    "เชิงสบายๆ": "น้ำเสียงเป็นกันเอง คำแนะนำสั้นๆ 1–2 ท่าเท่านั้น",
}

def build_prompt(mode: str) -> str:
    base = (
        "คุณเป็น AI Game Master ดูภาพกระดานแล้วช่วยผู้ใช้เข้าใจและตัดสินใจเดินหมากต่อไป "
        "ในเกมบนกระดาน ตอบกระชับและมีโครงสร้างเสมอ"
    )
    style = MODES.get(mode, MODES["เชิงกลยุทธ์"])
    return f"{base}\nสไตล์: {style}"

